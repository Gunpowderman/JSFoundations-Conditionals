// Define the numbers and the operation here

let Firstnumber = 8;
let Secondnumber = 5;
let Operation = "*";


// Write if-statements here

if (Operation == "+")
{
    console.log (Firstnumber + Secondnumber);
}
else if (Operation == "-")
{
    console.log (Firstnumber - Secondnumber);
}
else if (Operation == "*")
{
    console.log (Firstnumber * Secondnumber);
}
else if (Operation == "/")
{
    console.log (Firstnumber / Secondnumber);
}